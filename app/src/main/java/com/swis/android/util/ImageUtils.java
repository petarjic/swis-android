    public static void asyncCompressMultipleImage(final Context mContext, final ClipData data, final String identifier, final OnCompressDone delegate) {
        new Thread(new Runnable() {
            @Override
            public void run() {
                final List<String> listPath = new ArrayList<>();
                for (int i=0;i<data.getItemCount();i++){
                    String compressedImagePath = ImageUtils.compressImage(mContext,data.getItemAt(i).getUri(), null,identifier);
                    listPath.add(compressedImagePath);
                }
                new Handler(Looper.getMainLooper()).post(new Runnable() {
                    @Override
                    public void run() {
                        delegate.onSuccess(listPath);
                    }
                });
            }
        }).start();
    }

